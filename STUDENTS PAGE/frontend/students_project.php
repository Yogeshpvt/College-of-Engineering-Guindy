              <h3 style="margin-top:-37px;">CEG Launches ‘Young Research Fellow’ program for UG Students</h3>
              <p>College of Engineering Guindy has launched the ‘CEG Young Research Fellow’ (YRF) program to motivate students towards a career path in Research and application of deeper learning in other career tracks. Inspired by the highly successful ‘Super-UROP’ program at Massachusetts Institute of Technology (MIT), the YRF program is open to Third Year Undergraduate and Dual Degree Students of CEG. The program provides one-on-one interaction with the high calibre CEG faculty members, to participate in cutting edge research. The students will also benefit personally, through a ‘career and life coaching’ (CLIC) program concurrently being delivered by the 1979 alumni, closely curated by expert professional coaches. The Project is being funded by the Alumni Class of 1979. The MoU was signed on 10thJuly 2020 by Prof. Mahesh Panchagnula, Dean (Alumni and Corporate Relations),CEG, and twenty-four alumni of the 1979 batch across the world, on behalf of the entire Alumni Class of 1979. Underscoring the importance of impactful undergraduate research for the institute, Prof. Bhaskar Ramamurthi, Director, said, “CEG has long been known for producing world-class undergraduate students. Today, CEG is very much a research-intensive university where world-class research is a daily pursuit. This program blends these two strengths by giving select undergraduate students an opportunity to discover the excitement of research early.” This is a year-long program and up to twenty fellowships are being planned to be awarded in the first year. This is a highly selective program and applications will be examined by a senior faculty committee. The program will encourage multi-disciplinary projects. Speaking about this initiative, Prof. Anantha Chandrakasan, Dean of Engineering, MIT, who founded the ‘Super-UROP’ program at MIT, and advised on the creation of YRF program, said, “I wish you all success for the YRF program. Involving undergraduates in research will foster an appreciation for deeper learning and innovation, enabling their success in a wide array of career paths, including graduate studies.” The CEG YRF program has already received a lot of encouragement and shows of support, with industry and academic partners expressing keen interest. The year-long unique program is designed to provide immersive and intense scientific exploration opportunities to the students, with the participants executing research projects in any academic department or across departments for inter-disciplinary projects. Thanking the Alumni Class of 1979 for supporting the current batch of students through this program, Prof. Mahesh Panchagnula, Dean (Alumni and Corporate Relations), CEG, said, “The class of ’79 has championed a unique program that is both timely and innovative. At a time when India is talking of self-reliance, this program aims to create an interest in deep tech research among some of the best and brightest students in India.”
              </p>
              <h3 style="margin-top:-15px;">CEG Annual Tech Festival Technofes goes online this year</h3>
              <p> College of Engineering Guindy Annual Tech Festival Technofes is going to be conducted entirely online this year from 25th to 28th August 2021. This is the first time this tech-festival, which is one of the largest student-run events in the country, is going completely virtual.<br><br>
                  One of the unique features of Technofes 2021 will be the creation of a Virtual Rendering of the important sites of CEG Campus. This will enable a viewer to virtually walk through a location in CEG and provide as close to a real-life experience as possible. All the events will be available to the public free of cost. The total prize money for the contests will be worth around Rs. 20 lakh.<br><br>
                  Several eminent dignitaries are scheduled to address the 'Spotlight Lecture' Series, which brings in some of the most influential people in the world from diverse fields ranging from business magnates and successful entrepreneurs to Nobel Laureates.
