                   <h3 style="margin-bottom:-5px;">Aerospace Engineering</h3>
                   <img src="aerospace.jpg" alt="acs">
                   <p>Established in 1969, the Department of Aerospace Engineering stands at the forefront of fundamental and applied research and development, with a keen focus on scientific and social impact in the country. The department has contributed to manpower training via graduate research programs (PhD and Master of Science by research) as well as course-based programs (M.Tech, Dual Degree and B.Tech).</p>
                   <h3 style="margin-bottom:-5px; margin-top:-18px;">Biotechnology</h3>
                   <img src="biotech.jpg" alt="biotech">
                   <p>The Department of Biotechnology was founded in 2004 with a strong interdisciplinary research and teaching base in biological sciences and engineering. It carries out collaborative research with other departments within the Institute as well as other local and international institutes. Recently, the department also received significant funding and set up a cutting-edge facility for biotechnology research.</p>
                   <h3 style="margin-bottom:-5px;  margin-top:-18px;">Chemical Engineering</h3>
                   <img src="chemistry.jpg" alt="chem">
                   <p>Aiming to be a global centre of academic and research excellence in chemical engineering sciences and technology, the Department of Chemical Engineering has seen a dramatic increase in industrial collaboration and consultancy, research projects, research publications and faculty awards. Currently, it is establishing world-class facilities and research groups in the areas of energy, environment and materials.</p>
                   <h3 style="margin-bottom:-5px;  margin-top:-18px;">Civil Engineering</h3>
                   <img src="civil.jpg" alt="civil">
                   <p>Since its inception in 1959, the Department of Civil Engineering has grown into a full-fledged department with nationally and internationally acclaimed academic programs. Specialising in Planning, Design, Construction and Management, the department strives to be a pioneer in the country for teaching, research and consultancy in existing and emerging areas of civil engineering.</p>
                   <h3 style="margin-bottom:-5px;  margin-top:-18px;">Computer Science and Engineering</h3>
                   <img src="CS_CEG.jpg" alt="CS">
                   <p>Conceived in 1973 with the acquisition of an IBM System/370 (one of the most powerful computers in India at that time), the Department of Computer Science and Engineering now offers several attractive industry-sponsored Fellowships for outstanding PhD scholars, as well as support for participation in top-ranking international conferences. According to a 2014 Brown University study, IIT Madras was the fifth largest contributor of faculty members to computer science programs in U.S universities, including MIT, Stanford and Yale.</p>
                   <h3 style="margin-bottom:-5px;  margin-top:-18px;">Electrical Engineering</h3>
                   <img src="EEE.jpg" alt="EEE">
                   <p>The Department of Electrical Engineering is actively involved with research from absolute fundamentals and component design to system integration, deployment and commercialisation. Established in 1959, the department houses extensive fabrication, calibration and testing facilities to carry out academic projects, sponsored research and consultancy projects.</p>
                   <h3 style="margin-bottom:-5px;  margin-top:-18px;">Mechanical Engineering</h3>
                   <img src="mech.jpg" alt="mech">
                   <p>The Department of Mechanical Engineering is the largest of its kind in the country and one of the largest in Asia. As old as the Institute itself, the department has grown from strength to strength, evolving alongside major technological advances in India and the world and greatly impacting the Institute and society at large.</p>
                   <h3 style="margin-bottom:-5px;  margin-top:-18px; ">Ocean Engineering</h3>
                   <img src="ocean.jpg" alt="ocean">
                   <p>Originally conceived as the Ocean Engineering Centre (OEC) in 1977, the Department of Ocean Engineering (DOE) has since focused on offshore engineering, naval architecture, as well as research and development in wide areas of the ocean.
