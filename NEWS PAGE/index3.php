<h2><mark>Anna University Recruitment 2021: Apply 06 Project Assistant, Project Technician & Other Posts</mark></h2>
<p>Anna University has invited applications for the Project Assistant, Project Technician and other posts. Interested and eligible applicants can apply Anna University Job Notification 2021 through the prescribed applications format on or before 20 May 2021.
