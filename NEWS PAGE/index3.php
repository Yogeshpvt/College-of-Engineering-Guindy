<h2><mark>Anna University to conduct re-exam on May 25</mark></h2><br>
<p>Anna University re-exam will be held on May 25. The government has also ordered the university not to take exam fee again. The decision to conduct the re-exam has been taken following representations received from the B.E/ B.Tech students who had failed in large number. The re-exam will be held in online open book exam mode.</p><br>
<hr><h2><mark>No change in exam pattern,</mark></h2>
<p>Pre-Covid question paper system will be followed in this 3 hours online exam. Those who have cleared the February exam can also appear for this exam. As 70% of students have failed in the previous online proctored exam that was held in February, the failed attempt will not be considered as arrear. For students who failed the exam, the new Open book exam will be considered a fresh attempt.</p><br>
