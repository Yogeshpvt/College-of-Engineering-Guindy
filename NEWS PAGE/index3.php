<hr><h2> SPOT NEWS </h2>
<p>As companies shift operations to digital post the pandemic, there has been a spike in job offers at college campuses across the state in the past four months. Recruitment returned to pre-Covid levels post September as companies begun digitisation of operations that increased the projects for IT and IT product companies, said HR professionals.
Some top colleges said they have crossed job offers for the corresponding period last year and companies are willing to pay more for students.Online interviews were a new experience. It took one interview for us to adapt to the new environment, said G Pothikkannan, a final year student from College of Engineering, Guindy, who has received an offer from Wells Fargo.
When I heard that offers are being cancelled due to pandemic, I was worried. Thankfully, many companies turned up this year and our institute ensured a smooth digital hiring process, said Rajagopalan Gangadharan, a final year student from Vellore Institute of Technology.
