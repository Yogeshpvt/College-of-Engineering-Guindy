<h2>Query Optimization in Big data using Hadoop, HIVE and NEO4J</h2>
<img src="project3.jpg" alt="pro3 img">
<p>To store, retrieve and analyze Big data in an efficient way using both relational as well
as graph database. The Hadoop architecture encompassing Hive, HDFS and
Mapreduce is chosen as the relational database while Neo4J is taken as the graph
database.</p><br>
<p>The data to be queried has to be loaded into the hdfs storage space provided by
Hadoop. The database schema has to be created and the data should be loaded into the
database used by Hive which provides the interface for the SQL like syntax. The
queries have to be executed and the execution times are noted down. The optimization
techniques like indexing, hashing, bucketing and partitioning have to be applied and
the performance gains are noted. Also the graph database implementation in Neo4j
with data ingestion into it and the cypher queries have to be executed.</p><br><hr>


