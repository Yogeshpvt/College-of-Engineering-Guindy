development, healthy ecosystems and for human survival itself. Water is finite and
irreplaceable resource that is fundamental to human being. It is only renewable if well
managed .In our point of view CEG campus being a wide and eco-friendly campus has been
identified with a whopping sum of bore wells and open wells all within it. But still the water
resources within the CEG campus are known to be low yielding at adverse times. In addition
to this the water quality of the various water resources remains a question to be answered
.Several parameters were discussed to get the best possible results and a set of observation
wells were classified based on these parameters. Based on the observations made at the
indicated locations, we have developed an estimation of the water levels and water quality for
the entire CEG campus. This estimation can be approximated to the total water consumption
in CEG campus per day .Knowing the yield of different water resources within the campus
and the consumption of these resources, the additional amount of water required to meet the
demands of the campus can be assessed .In addition to this the water quality was analyzed
and constructive suggestions have been provided..This in fact would give a clear picture of
the availability and sustainability of water resources in CEG campus. The objectives,
