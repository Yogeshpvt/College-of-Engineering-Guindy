<p>The CEG division of the National Sports Organization (NSO), works towards the development of sports and focus on nurturing the athletic talent in CEGians. The NSO also conducts an annual camp to concentrate on intensive sport training for the attendees. This division supports and trains the various indoor and outdoor sport teams that represent the college.The NSO cell functions in association with the Anna University Sports Board to carry out its goals.</p>
