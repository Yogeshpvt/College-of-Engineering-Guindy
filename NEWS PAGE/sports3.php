<h2>Sports Zone</h2>
<p><mark>Sports Zones of the University :</mark>
All the Institutions (Constituent / Govt. / Govt. Aided and Self-Financing) under Anna University are divided into Nineteen Zones, each zone involving around 25-30 colleges. Every zone has Zonal Sports Coordinator responsible for the sports activities of the zone.
<br><br><mark>Conduct of Sports & Games :</mark>
The various Sports and Games are categorized under Nineteen Zone, and One-Zone basis, depending upon the expected participation and infrastructure requirements. The Zonal tournaments are followed by Inter-zonal tournaments.
<br><br><mark>University Teams :</mark>
For Sports and Games under Nineteen-Zone basis, the Zonal tournaments are conducted in all the Nineteen Zones, followed by Inter-Zonal tournaments. The University Team Selection Committee will select the University team based on the performance of players in the Inter-Zone / One-zone / Two-Zone basis tournaments. The selected University teams will participate in coaching camps prior their participation in Inter-University tournaments.
<br><br><mark>Paralympic Sports Meet :</mark>
To take care of the physically challenged students of Anna University who have interest in sports and games, special tournaments are organized once in a year, under one-zone basis, in athletics and other games, depending on the entries received.
<br><br><mark>Annual Sports Award Function :</mark>
The Anna University Sports Board organizes the Annual Sports Award Function to honour the outstanding sportspersons of Anna University with special trophies for their achievements in Inter-University / National / Inter-National tournaments. The best performing colleges at Zonal level and Inter-Zonal level will be honored with Championship trophies during the Award function.
<br><br><mark>National Sports Organization :</mark>
Anna University strives constantly in total personality development of all its students so that they can serve the community towards its advancement. In this direction, as part of Regulations 2005, all the undergraduate students should enroll on admission to one of the character development programmes, namely National Cadet Corps (N.C.C.), Youth Red Cross ( Y R C) National Social Service (N.S.S.) and National Sports Organization (N.S.O.) under the personality Character Development Programme. Over the years, we have found that these activities have brought discernible change in the attitude of students only towards their fellow but also towards values of life.</P>
